	  <h2>Current Research</h2>
	  <ul>
	    I am interested in Kinetic Inductance Detectors (KIDs) as candidate 
			detectors for future Cosmic Microwave Background telescopes, including 
			<a href="http://arxiv.org/pdf/1010.0645.pdf">QUBIC</a>.  My work focuses 
      on pushing lumped element KIDs to the lowest possible optical frequencies.  
      I design KIDs using EM simulation software and semi-analytic calculations, fabricate prototype arrays 
      in the Detector Development Laboratory cleanroom facility at NASA Goddard, and test 
      the arrays in our ADR-cooled laboratory cryostat with our novel 100 GHz window-free, 
      vacuum waveguide feedthrough-free optical coupling scheme.  The goal is to demonstrate 
      the sensitivity achievable with simple quasi-lumped element direct-absorbing KIDs at 100 GHz optical frequency.  
      <br> <br>      
		
	  </ul>
		
