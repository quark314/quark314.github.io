    I'm bringing to the pole with me.  In addition to that, there's an opportunity to leave things in Christchirch at the CDC (clothing distribution center) where they issue 
    all the cold weather gear, so I'll probably want to leave a small amount of summer clothes or other items that I'll want for my stay in NZ or for the travel days to- 
