    So you've got the job, you've been physically qualified... now what? <br>
    Well now you've got to tie up all the loose ends in your life and get everything in your life ready to put on hold for almost a year.  
    <br><br>
    
    For me, the biggest thing is finishing up the experiment I'm currently working on.  I've got just a month and a half left, so it's
    going to be a tight squeeze.  I'll talk more about the science I've been doing over the past few years in a future blog post.  Outside
    of that, I also have to find someone to take over the remaining 7 1/2 months of my lease, move out of my apartment, and store all my stuff.
    Since internet and other communications are potentially unreliable at the pole, I will be trying to get all of my financial things automated
    before I leave so hopefully nothing has to be dealt with.  This is mostly true of how I have things already, but there's some unusualy odds 
    and ends that arise because of the length of time I'll be gone.  For instance, I'll be away not only during normal tax-filing season, but also
    I'll <em>still</em> be gone 6 months later when a normal tax filing extension would end.  One of my credit cards is set to expire while I'm gone, 
    so I'll have to ask them to issue a replacement early.  I'll have to figure out how to renew my car's registration remotely when it expires in August.  
    Lots of weird little odds and ends like that.  Just in case I miss something important, or if something unanticipated comes up, I've given one of 
    my family members power of attorney, so they can help out with any miscellaneous things that come up that I can't fix myself remotely.  
    
